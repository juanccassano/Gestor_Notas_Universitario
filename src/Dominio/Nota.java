package Dominio;

public class Nota {
	
	int Legajo;
	int Nota;
	String Instancia;
	int IDCurso;
	public int getLegajo() {
		return Legajo;
	}
	public void setLegajo(int legajo) {
		Legajo = legajo;
	}
	public int getNota() {
		return Nota;
	}
	public void setNota(int nota) {
		Nota = nota;
	}
	public String getInstancia() {
		return Instancia;
	}
	public void setInstancia(String instancia) {
		Instancia = instancia;
	}
	public int getIDCurso() {
		return IDCurso;
	}
	public void setIDCurso(int iDCurso) {
		IDCurso = iDCurso;
	}
	
	
	

}
