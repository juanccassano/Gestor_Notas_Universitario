package Dominio;

public class Localidad {
int ID;
int IDProvincia;
String Nombre;
public int getID() {
	return ID;
}
public void setID(int iD) {
	ID = iD;
}
public int getIDProvincia() {
	return IDProvincia;
}
public void setIDProvincia(int iDProvincia) {
	IDProvincia = iDProvincia;
}
public String getNombre() {
	return Nombre;
}
public void setNombre(String nombre) {
	Nombre = nombre;
}



}
